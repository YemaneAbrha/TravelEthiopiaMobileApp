class Availiable {
  final int id;
  final String from;
  final String to;
  final String departure_date;
  final String departure_time;
  final String bus_name;
  final String bus_description;
  final double fee;
  final int availiable_seat;
  Availiable(
    this.id,
    this.from,
    this.to,
    this.departure_date,
    this.departure_time,
    this.bus_name,
    this.bus_description,
    this.fee,
    this.availiable_seat,
  );
}
